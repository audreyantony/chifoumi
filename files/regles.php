<p><br><br><br><br>Le chifoumi est un jeu de hasard effectué avec les mains et opposant un ou plusieurs joueurs. Il possède de nombreux noms alternatifs, le nom Pierre-Papier-Ciseaux est également une appellation courante.
    <br><br><br><br>
    Les deux joueurs choisissent simultanément un des trois coups possibles soit en le symbolisant de la main, soit en cliquant sur son symbole.
    <br><br><br><br>
    De façon générale, la pierre bat les ciseaux (en les émoussant), les ciseaux battent la feuille (en la coupant), la feuille bat la pierre (en l'enveloppant). Ainsi chaque coup bat un autre coup, fait match nul contre le deuxième (son homologue) et est battu par le troisième.<br><br><br><br><br><br><br><br><br><br><br> Wikipedia®
